!==============================================================================!
  subroutine Vector_Allocate(A, n)
!------------------------------------------------------------------------------!
  implicit none
!---------------------------------[Arguments]----------------------------------!
  class(Vector_Type)  :: A
  integer, intent(in) :: n
!==============================================================================!

  ! Store the length
  A % len = n

  ! Allocate the memory
  allocate(A % val(n))

  end subroutine
