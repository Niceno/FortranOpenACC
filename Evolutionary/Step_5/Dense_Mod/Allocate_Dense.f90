!==============================================================================!
  subroutine Allocate_Dense(A, n)
!------------------------------------------------------------------------------!
  implicit none
!---------------------------------[Arguments]----------------------------------!
  class(Dense_Type)   :: A
  integer, intent(in) :: n
!==============================================================================!

  ! Store the length
  A % len = n

  ! Allocate the memory
  allocate(A % val(n, n))

  end subroutine
